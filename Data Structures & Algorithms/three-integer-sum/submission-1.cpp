class Solution {
public:
    vector<vector<int>> threeSum(vector<int>& nums) {

        sort(nums.begin(),nums.end());
        vector<vector<int>>res;

        for(int i=0;i<nums.size();i++)
        {
           if(nums[i]>0)  break;

           if(i>0 && nums[i]==nums[i-1]) continue;

           int l=i+1, r=nums.size()-1;

           while(l<r)
           {
            int sum=nums[i] + nums[l] +nums[r];

            if(sum==0)
            {
                res.push_back({nums[i],nums[l],nums[r]});

                l++;
                r--;

                while(l<r && nums[l]==nums[l-1])
                {
                    l++;
                }

                while(l<r && nums[r]==nums[r+1])
                {
                    r--;
                }
            }

            else if (sum>0)
            {
                r--;
            }

            else
            {
                l++;
            }
           }

        }
        return res;
        
    }
};

/*
========================================================================
                        3Sum (Two Pointer Approach)
========================================================================

সমস্যা:
--------
Array থেকে এমন সব unique triplet বের করতে হবে যাদের যোগফল = 0।

উদাহরণ:
nums = [-1,0,1,2,-1,-4]

Output:
[[-1,-1,2],[-1,0,1]]

========================================================================
Approach (বাংলায় ব্যাখ্যা)
========================================================================

১) প্রথমে পুরো array sort করি।

sort(nums.begin(), nums.end());

কেন sort করি?
--------------
১. Two Pointer Technique ব্যবহার করা যায়।
২. Duplicate element সহজে skip করা যায়।
৩. Pointer move করলে sum বাড়বে নাকি কমবে তা সহজেই বোঝা যায়।

উদাহরণ:

Before Sorting:
[-1,0,1,2,-1,-4]

After Sorting:
[-4,-1,-1,0,1,2]


========================================================================
Step-1: প্রথম সংখ্যাটি Fix করা
========================================================================

for(int i=0;i<nums.size();i++)

প্রতিবার nums[i] কে triplet-এর প্রথম সংখ্যা হিসেবে fix করি।

এরপর বাকি দুইটি সংখ্যা Two Pointer দিয়ে খুঁজি।

অর্থাৎ,

nums[i] + nums[l] + nums[r] = 0


========================================================================
Step-2: যদি nums[i] > 0 হয় তাহলে break করি
========================================================================

if(nums[i] > 0)
    break;

কারণ array sorted।

যদি বর্তমান সংখ্যা positive হয়,
তাহলে এর পরের সব সংখ্যাও positive হবে।

তিনটি positive সংখ্যা যোগ করে কখনোই 0 পাওয়া সম্ভব না।

তাই loop বন্ধ করে দিই।

উদাহরণ:

[1,2,3,4]

1+2+3 = 6

আর কোনোভাবেই 0 হবে না।


========================================================================
Step-3: Duplicate First Element Skip
========================================================================

if(i>0 && nums[i]==nums[i-1])
    continue;

কেন?

ধরি,

[-4,-1,-1,0,1,2]

প্রথম -1 দিয়ে সব triplet বের করেছি।

দ্বিতীয় -1 দিয়ে আবার শুরু করলে একই triplet
আবার পাওয়া যাবে।

তাই duplicate first element skip করি।


========================================================================
Step-4: Two Pointer Initialize
========================================================================

int l = i+1;
int r = nums.size()-1;

l = দ্বিতীয় সংখ্যা
r = তৃতীয় সংখ্যা

উদাহরণ

[-4,-1,-1,0,1,2]

      i  l        r


========================================================================
Step-5: যতক্ষণ l < r ততক্ষণ Search
========================================================================

while(l<r)

প্রতিবার

sum = nums[i] + nums[l] + nums[r]

হিসাব করি।


========================================================================
Case-1: sum == 0
========================================================================

Triplet পাওয়া গেছে।

res.push_back({nums[i],nums[l],nums[r]});

তারপর

l++;
r--;

করি যাতে নতুন pair খুঁজতে পারি।

এরপর duplicate skip করি।

while(l<r && nums[l]==nums[l-1])
    l++;

while(l<r && nums[r]==nums[r+1])
    r--;

এভাবে একই triplet দ্বিতীয়বার result-এ যোগ হয় না।


========================================================================
Case-2: sum > 0
========================================================================

মানে sum অনেক বড়।

তাই বড় সংখ্যাটি ছোট করতে হবে।

কারণ array sorted।

সেজন্য

r--;

করি।

Right pointer বামে গেলে ছোট সংখ্যা পাওয়া যায়।


========================================================================
Case-3: sum < 0
========================================================================

মানে sum অনেক ছোট।

তাই বড় সংখ্যা দরকার।

সেজন্য

l++;

করি।

Left pointer ডানে গেলে বড় সংখ্যা পাওয়া যায়।


========================================================================
Dry Run (সংক্ষেপে)
========================================================================

Input:

[-1,0,1,2,-1,-4]

Sort করার পরে

[-4,-1,-1,0,1,2]

i = 0

-4 + (-1) + 2 = -3

Sum ছোট

l++

----------------------------------

i = 1

-1 + (-1) + 2 = 0

Triplet পাওয়া গেল

[-1,-1,2]

----------------------------------

l++, r--

-1 + 0 + 1 = 0

আরেকটি Triplet

[-1,0,1]

Result

[
 [-1,-1,2],
 [-1,0,1]
]


========================================================================
Time Complexity Analysis
========================================================================

ধরি array-এর size = n

১) Sorting

O(n log n)

----------------------------------

২) Outer Loop

for loop সর্বোচ্চ n বার চলবে।

O(n)

----------------------------------

৩) Two Pointer

প্রতিটি i-এর জন্য
left এবং right pointer
একবারই traverse করে।

কোন pointer পিছনে ফিরে আসে না।

তাই

O(n)

----------------------------------

Total Time Complexity

O(n log n) + O(n × n)

= O(n²)

কারণ O(n²) সবচেয়ে বড় term।

Final Time Complexity = O(n²)


========================================================================
Space Complexity Analysis
========================================================================

Extra Variable:

i
l
r
sum

এগুলো constant সংখ্যক variable।

অর্থাৎ Extra Space = O(1)

Result vector-এর space সাধারণত complexity-তে ধরা হয় না,
কারণ সেটি output store করার জন্য ব্যবহৃত হয়।

Final Space Complexity = O(1)


========================================================================
Final Complexity
========================================================================

Time Complexity  : O(n²)

Space Complexity : O(1)

========================================================================
*/
